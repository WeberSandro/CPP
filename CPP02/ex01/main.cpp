#include <iostream>
#include "Fixed.hpp"

int main(void)
{
    Fixed a;                // Construtor padrão chamado
    Fixed const b(10);      // Construtor que aceita um inteiro
    Fixed const c(42.42f);  // Construtor que aceita um float
    Fixed const d(b);       // Construtor de cópia chamado

    a = Fixed(1234.4321f);  // Construtor com float + operador de atribuição
    std::cout << "a is " << a << std::endl;
    std::cout << "b is " << b << std::endl;
    std::cout << "c is " << c << std::endl;
    std::cout << "d is " << d << std::endl;
    std::cout << "a is " << a.toInt() << " as integer" << std::endl;
    std::cout << "b is " << b.toInt() << " as integer" << std::endl;
    std::cout << "c is " << c.toInt() << " as integer" << std::endl;
    std::cout << "d is " << d.toInt() << " as integer" << std::endl;
    return 0;
}
